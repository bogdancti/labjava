public class Car {
    public String name;
    public String color;
    public Integer year_fabrication;
    public Double engine;
    public Integer door_numbers;

    public Car(String name, String color, Integer year_fabrication, Double engine, Integer door_numbers){
        this.name=name;
        this.color=color;
        this.year_fabrication=year_fabrication;
        this.engine=engine;
        this.door_numbers=door_numbers;
    }
}
